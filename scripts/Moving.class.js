class MovingObject {

    x = 0;
    y = 0;

    iniX = 0;
    iniY = 0;

    speedX = 0;
    speedY = 0;


    setPos(xPos, yPos){
        this.x = xPos;
        this.y = yPos;
    }

    setIni(inX, inY){
        this.iniX = inX;
        this.iniY = inY;
    }

    setSpeed(xSpe, ySpe){
        this.speedX = xSpe;
        this.speedY = ySpe
    }



}